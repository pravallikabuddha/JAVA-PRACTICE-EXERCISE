// stsatic and local variables
public class staticvar {
   // static variables
	static String name;
   static int i;
   static float f;
   static double d;

   
   public static void main(String[] args) {
    // local variable
    int k=1;

    System.out.println(name);
    System.out.println(i);
    System.out.println(f);
    System.out.println(d);
    System.out.println(k);
  }
     

}